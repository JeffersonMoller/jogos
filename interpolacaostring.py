print("Tentativa {} de {}".format(3, 10))  # Mostra Tentativa 3 de 10

print("Tentativa {1} de {0}".format(3, 10))  # Mostra Tentativa 10 de 3

print("R$ {}".format(1.59))  # Mostra R$ 1.59

print("R$ {:f}".format(1.59))  # Mostra R$ 1.590000 - Float

print("R$ {:.2f}".format(1.59))  # Mostra R$ 1.59 - Float

print("R$ {:.2f}".format(1.5))  # Mostra R$ 1.50 - Float

print("R$ {:7.2f}".format(1.5))  # Mostra R$    1.50 - Float

print("R$ {:07.2f}".format(1.5))  # Mostra R$ 0001.50 - Float

print("R$ {:07d}".format(1))  # Mostra R$ 0000001 - Inteiro

print("Data {:02d}/{:02d}".format(19, 4))  # Mostra Data 19/04 - Inteiro
